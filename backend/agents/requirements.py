def validate_project_name(name: str) -> list[ValidationError]:
    """Validate project name follows SAP conventions."""
    errors: list[ValidationError] = []

    if not name:
        errors.append({
            "agent": "requirements",
            "code": "PROJECT_NAME_REQUIRED",
            "message": "Project name is required",
            "field": "project_name",
            "severity": "error",
        })
        return errors

    if len(name) < 3:
        errors.append({
            "agent": "requirements",
            "code": "PROJECT_NAME_TOO_SHORT",
            "message": "Project name must be at least 3 characters",
            "field": "project_name",
            "severity": "error",
        })

    if len(name) > 50:
        errors.append({
            "agent": "requirements",
            "code": "PROJECT_NAME_TOO_LONG",
            "message": "Project name must be 50 characters or less",
            "field": "project_name",
            "severity": "error",
        })

    if not name[0].isalpha():
        errors.append({
            "agent": "requirements",
            "code": "PROJECT_NAME_INVALID_START",
            "message": "Project name must start with a letter",
            "field": "project_name",
            "severity": "error",
        })

    if not all(c.isalnum() or c in "-_ " for c in name):
        errors.append({
            "agent": "requirements",
            "code": "PROJECT_NAME_INVALID_CHARS",
            "message": "Project name can only contain letters, numbers, spaces, hyphens, and underscores",
            "field": "project_name",
            "severity": "error",
        })

    return errors


def validate_entity_name(name: str) -> list[ValidationError]:
    """Validate entity name follows CDS conventions."""
    errors: list[ValidationError] = []

    if not name:
        errors.append({
            "agent": "requirements",
            "code": "ENTITY_NAME_REQUIRED",
            "message": "Entity name is required",
            "field": "entity_name",
            "severity": "error",
        })
        return errors

    # Must be PascalCase (start with uppercase)
    if not name[0].isupper():
        errors.append({
            "agent": "requirements",
            "code": "ENTITY_NAME_NOT_PASCAL_CASE",
            "message": f"Entity name '{name}' must start with uppercase (PascalCase)",
            "field": "entity_name",
            "severity": "warning",
        })

    # Only alphanumeric
    if not name.isalnum():
        errors.append({
            "agent": "requirements",
            "code": "ENTITY_NAME_INVALID_CHARS",
            "message": f"Entity name '{name}' can only contain letters and numbers",
            "field": "entity_name",
            "severity": "error",
        })

    return errors


def validate_entities(entities: list[EntityDefinition]) -> list[ValidationError]:
    """Validate entity definitions."""
    errors: list[ValidationError] = []

    if not entities:
        errors.append({
            "agent": "requirements",
            "code": "NO_ENTITIES",
            "message": "At least one entity must be defined",
            "field": "entities",
            "severity": "error",
        })
        return errors

    entity_names = set()
    for entity in entities:
        name = entity.get("name", "")

        # Check for duplicate names
        if name in entity_names:
            errors.append({
                "agent": "requirements",
                "code": "DUPLICATE_ENTITY_NAME",
                "message": f"Duplicate entity name: {name}",
                "field": "entities",
                "severity": "error",
            })
        entity_names.add(name)

        # Validate entity name
        errors.extend(validate_entity_name(name))

        # Check for key field
        fields = entity.get("fields", [])
        has_key = any(f.get("key", False) for f in fields)
        if fields and not has_key:
            errors.append({
                "agent": "requirements",
                "code": "NO_KEY_FIELD",
                "message": f"Entity '{name}' must have at least one key field",
                "field": "entities",
                "severity": "error",
            })

    return errors


def _ensure_entity_quality(entities: list) -> list:
    """Post-process LLM output to ensure minimum quality standards."""
    for entity in entities:
        fields = entity.get("fields", [])

        # Ensure ID field exists as first field
        has_id = any(f.get("name") == "ID" and f.get("key") for f in fields)
        if not has_id:
            fields.insert(0, {"name": "ID", "type": "UUID", "key": True, "nullable": False})
            entity["fields"] = fields

        # Ensure aspects are set
        if not entity.get("aspects"):
            entity["aspects"] = ["cuid", "managed"]

        # Ensure String fields have length
        for field in fields:
            if field.get("type") == "String" and not field.get("length"):
                field["length"] = 100  # sensible default
            if field.get("type") == "Decimal":
                if not field.get("precision"):
                    field["precision"] = 12
                if not field.get("scale"):
                    field["scale"] = 2

    return entities


