async def requirements_agent(state: BuilderState) -> BuilderState:
    """
    Requirements & Domain Agent (LLM-First)

    ALWAYS uses LLM to generate domain-specific entity definitions.
    No hardcoded domain templates — every project gets a unique,
    contextually appropriate data model.

    Processes:
    1. Validates project configuration
    2. Uses LLM to generate entities, relationships, and business rules
    3. Post-processes output for quality assurance
    4. Validates final entities

    Returns updated state with entities, relationships, and business rules.
    """
    logger.info("Starting Requirements Agent (LLM-First)")

    now = datetime.utcnow().isoformat()
    errors: list[ValidationError] = []

    # Update state
    state["current_agent"] = "requirements"
    state["updated_at"] = now
    state["current_logs"] = []

    log_progress(state, "Analyzing project configuration...")

    # ==========================================================================
    # Step 1: Validate project configuration
    # ==========================================================================
    project_name = state.get("project_name", "")
    errors.extend(validate_project_name(project_name))

    if any(e["severity"] == "error" for e in errors):
        state["validation_errors"] = errors
        logger.error(f"Validation failed: {errors}")
        log_progress(state, f"Validation failed: {errors[0]['message']}")
        return state

    # ==========================================================================
    # Step 2: Determine what generation is needed
    # ==========================================================================
    domain_type = state.get("domain_type", DomainType.CUSTOM.value)
    description = state.get("project_description", "")
    provider = state.get("llm_provider")
    user_entities = state.get("entities", [])

    # Determine if user entities have full field definitions
    entities_have_fields = user_entities and all(
        len(e.get("fields", [])) > 1 for e in user_entities  # More than just ID
    )

    if entities_have_fields:
        # ======================================================================
        # Path A: User provided complete entities — validate only
        # ======================================================================
        log_progress(state, "Validating user-defined entities...")
        errors.extend(validate_entities(user_entities))

        # Still use LLM to generate relationships and business rules if missing
        if not state.get("relationships") or not state.get("business_rules"):
            log_progress(state, "Generating relationships and business rules for existing entities...")
            entity_names = [e.get("name", "") for e in user_entities]
            entity_descs = [f"{e.get('name', '')}: {e.get('description', '')}" for e in user_entities]

            field_summaries = []
            for e in user_entities:
                fields = e.get("fields", [])
                field_names = []
                for f in fields:
                    if isinstance(f, dict):
                        field_names.append(f.get("name", ""))
                    else:
                        field_names.append(str(f))
                field_summaries.append({
                    "name": e.get("name", ""),
                    "fields": field_names
                })
            
            try:
                fields_json = json.dumps(field_summaries, indent=2)
            except Exception:
                fields_json = "[]"
                
            try:
                entities_json = json.dumps(user_entities)
            except Exception:
                entities_json = "[]"

            rel_prompt = f"""Given these existing entities in a {project_name} application:

{chr(10).join(entity_descs)}

Entity Fields (summary):
{fields_json}

Generate relationships between these entities and business rules.

Respond with ONLY valid JSON:
{{
  "entities": {entities_json},
  "relationships": [...],
  "business_rules": [...]
}}"""

            result = await _generate_with_retry(rel_prompt, REQUIREMENTS_SYSTEM_PROMPT, provider, state, max_retries=2)
            if result:
                state["relationships"] = result.get("relationships", state.get("relationships", []))
                state["business_rules"] = result.get("business_rules", state.get("business_rules", []))
                log_progress(state, f"Generated {len(state.get('relationships', []))} relationships, {len(state.get('business_rules', []))} business rules.")

    elif user_entities and not entities_have_fields:
        # ======================================================================
        # Path B: User provided entity names only — LLM generates fields
        # ======================================================================
        entity_names = [e.get("name", "") for e in user_entities]
        log_progress(state, f"🧠 Using LLM to generate full definitions for: {', '.join(entity_names)}...")

        # Build context about entity names
        entity_context = f"User has specified these entity names: {', '.join(entity_names)}"
        additional_context = f"""IMPORTANT: You MUST include ALL of these entities in your response: {', '.join(entity_names)}
You may also add related entities if they make business sense (e.g., line item entities for header entities)."""

        prompt = DOMAIN_ANALYSIS_PROMPT.format(
            project_name=project_name,
            description=description or f"A {domain_type} business application with entities: {', '.join(entity_names)}",
            domain_type=domain_type,
            entity_context=entity_context,
            additional_context=additional_context,
        )

        result = await _generate_with_retry(prompt, REQUIREMENTS_SYSTEM_PROMPT, provider, state)

        if result:
            generated_entities = _ensure_entity_quality(result.get("entities", []))
            state["entities"] = generated_entities
            state["relationships"] = result.get("relationships", [])
            state["business_rules"] = result.get("business_rules", [])
            log_progress(state, f"✅ LLM generated {len(generated_entities)} entities with complete field definitions.")
        else:
            # This is the only fallback — but it NEVER uses templates
            log_progress(state, "⚠️ LLM generation failed after all retries. Generating minimal entities.")
            errors.append({
                "agent": "requirements",
                "code": "LLM_GENERATION_FAILED",
                "message": "LLM failed to generate entities. Minimal fallback applied — consider re-running.",
                "field": None,
                "severity": "warning",
            })
            # Minimal fallback: just ensure entities have ID field
            for entity in user_entities:
                if not entity.get("fields"):
                    entity["fields"] = [
                        {"name": "ID", "type": "UUID", "key": True, "nullable": False},
                        {"name": "name", "type": "String", "length": 100, "nullable": False},
                        {"name": "description", "type": "LargeString", "nullable": True},
                        {"name": "status", "type": "String", "length": 20, "nullable": False, "default": "'Active'"},
                    ]
                    entity["aspects"] = entity.get("aspects", ["cuid", "managed"])
            state["entities"] = user_entities

    else:
        # ======================================================================
        # Path C: No entities provided — LLM generates everything from description
        # ======================================================================
        if description or domain_type != DomainType.CUSTOM.value:
            effective_description = description or f"A comprehensive {domain_type} management application"
            log_progress(state, f"🧠 Using LLM to design entire data model for: {project_name}...")

            prompt = DOMAIN_ANALYSIS_PROMPT.format(
                project_name=project_name,
                description=effective_description,
                domain_type=domain_type,
                entity_context="No entities specified — you must identify the right entities for this domain.",
                additional_context="""Generate 4-8 entities that form a complete, realistic business application.
Think about what entities a real enterprise would need — not a basic tutorial.
Include transactional entities, master data, and line-item entities.""",
            )

            result = await _generate_with_retry(prompt, REQUIREMENTS_SYSTEM_PROMPT, provider, state)

            if result:
                generated_entities = _ensure_entity_quality(result.get("entities", []))
                state["entities"] = generated_entities
                state["relationships"] = result.get("relationships", [])
                state["business_rules"] = result.get("business_rules", [])
                log_progress(state, f"✅ LLM designed {len(generated_entities)} entities for {domain_type} domain.")
            else:
                log_progress(state, "❌ LLM generation failed after all retries.")
                errors.append({
                    "agent": "requirements",
                    "code": "LLM_GENERATION_FAILED",
                    "message": "Could not generate entities. Please check your LLM API key and try again.",
                    "field": None,
                    "severity": "error",
                })
        else:
            log_progress(state, "No entities or description provided.")
            errors.append({
                "agent": "requirements",
                "code": "NO_INPUT",
                "message": "Please provide either entity names or a project description",
                "field": None,
                "severity": "error",
            })

    # ==========================================================================
    # Step 3: Final validation
    # ==========================================================================
    log_progress(state, "Performing final validation...")
    errors.extend(validate_entities(state.get("entities", [])))

    # ==========================================================================
    # Step 4: Set main entity for Fiori if not set
    # ==========================================================================
    if not state.get("fiori_main_entity") and state.get("entities"):
        state["fiori_main_entity"] = state["entities"][0].get("name", "")

    # ==========================================================================
    # Step 5: Update state
    # ==========================================================================
    state["validation_errors"] = errors

    # Record execution
    state["agent_history"] = state.get("agent_history", []) + [{
        "agent_name": "requirements",
        "status": "completed" if not any(e["severity"] == "error" for e in errors) else "failed",
        "started_at": now,
        "completed_at": datetime.utcnow().isoformat(),
        "duration_ms": None,
        "error": None if not errors else str(errors[0]["message"]) if errors else None,
        "logs": state.get("current_logs", []),
    }]

    log_progress(state, "Requirements analysis complete.")
    logger.info(f"Requirements Agent completed. Entities: {len(state.get('entities', []))}, Errors: {len(errors)}")

    return state
