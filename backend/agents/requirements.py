async def _generate_with_retry(
    prompt: str,
    system_prompt: str,
    provider: str | None,
    state: BuilderState,
    max_retries: int = 3,
) -> dict | None:
    """
    Call LLM with retry logic and self-healing.
    If JSON parsing fails, feeds the error back to the LLM.
    """
    from backend.agents.llm_utils import get_complexity_prompt
    
    llm_manager = get_llm_manager()
    last_error = None

    # Inject complexity-level instructions into the prompt
    complexity_instructions = get_complexity_prompt(state)
    current_prompt = f"{complexity_instructions}\n\n{prompt}"

    for attempt in range(max_retries):
        try:
            response = await llm_manager.generate(
                prompt=current_prompt,
                system_prompt=system_prompt,
                provider=provider,
                temperature=0.1 if attempt == 0 else 0.05,  # Lower temp on retries
            )

            parsed = _parse_llm_json(response)

            if parsed and isinstance(parsed, dict):
                # Basic structural validation
                entities = parsed.get("entities", [])
                if entities and len(entities) > 0:
                    log_progress(state, f"✅ LLM generated {len(entities)} entities (attempt {attempt + 1}).")
                    return parsed
                else:
                    last_error = "Response contained empty entities list"
                    log_progress(state, f"⚠️ Attempt {attempt + 1}: {last_error}. Retrying...")
            else:
                last_error = "Could not parse JSON from response"
                log_progress(state, f"⚠️ Attempt {attempt + 1}: {last_error}. Retrying...")

            # Build correction prompt for next attempt
            if attempt < max_retries - 1:
                current_prompt = f"""Your previous response could not be used. Error: {last_error}

Previous response (first 500 chars): {response[:500]}

CRITICAL: You MUST respond with ONLY valid JSON. No markdown, no explanation, no text before or after the JSON.
Start your response with {{ and end with }}.

{prompt}"""

        except Exception as e:
            last_error = str(e)
            is_rate_limit = "429" in last_error or "rate" in last_error.lower() or "quota" in last_error.lower()
            
            if is_rate_limit:
                backoff = (5 * (2 ** attempt)) + random.uniform(0.5, 2.0)
                logger.warning(f"Rate limit hit (attempt {attempt + 1}). Backing off {backoff:.1f}s...")
                log_progress(state, f"⚠️ Attempt {attempt + 1} error: Error code: 429 - {str(e)[:80]}. Retrying...")
                await asyncio.sleep(backoff)
            else:
                logger.warning(f"LLM attempt {attempt + 1} failed: {e}")
                log_progress(state, f"⚠️ Attempt {attempt + 1} error: {str(e)[:100]}. Retrying...")
                await asyncio.sleep(2)

            if attempt < max_retries - 1:
                current_prompt = f"{complexity_instructions}\n\n{prompt}"  # Reset with complexity prefix

    logger.error(f"All {max_retries} LLM attempts failed. Last error: {last_error}")
    return None


